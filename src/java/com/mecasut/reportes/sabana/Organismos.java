package com.mecasut.reportes.sabana;

/**
 *
 * @author DIN-A-C14-02
 */
public class Organismos {
    
    private int id;
    private String nombre;
    private String sigla;
    private boolean extranjero;
    private String pais;

    public Organismos() {
    }

    public Organismos(int id, String nombre, String sigla, boolean extranjero, String pais) {
        this.id = id;
        this.nombre = nombre;
        this.sigla = sigla;
        this.extranjero = extranjero;
        this.pais = pais;
    }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getSigla() {
        return sigla;
    }

    public void setSigla(String sigla) {
        this.sigla = sigla;
    }

    public boolean isExtranjero() {
        return extranjero;
    }

    public void setExtranjero(boolean extranjero) {
        this.extranjero = extranjero;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

}
